public class ArrayBasic {
    public static void main(String[] args) {
        // array intialization
        int arr[] = new int[4];
        // int which type store the element you can use it = ex char and float etc ..
        // updatation
        arr[0] = 2;
        arr[1] = 3;
        arr[2] = 4;
        arr[3] = 5;

        // print
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }

    }

}
